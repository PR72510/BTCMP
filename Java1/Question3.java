import java.util.Scanner;

public class Sol3 {


    public static void main(String []args){

        int L1, res_L;

        Scanner s = new Scanner(System.in);
        System.out.println("Enter the string");
        String input_String = s.nextLine();

        System.out.println("Enter the Character");
        char c = s.next().charAt(0);

        System.out.println("Length of Input String " + input_String.length());
        L1 = input_String.replaceAll(String.valueOf(c),"").length();

        res_L = input_String.length() - L1;

        System.out.println("No. of occurence " + res_L);
    }

}
